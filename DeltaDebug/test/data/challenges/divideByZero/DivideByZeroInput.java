package data.challenges.divideByZero;

public class DivideByZeroInput {
	private int dividend, divider;
	
	public DivideByZeroInput(int dividend, int divider) {
		this.dividend = dividend;
		this.divider = divider;
	}
	
	public int getDividend() {
		return dividend;
	}
	
	public int getDivider() {
		return divider;
	}
}
